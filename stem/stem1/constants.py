F = 96485.3321 # Faraday constant
NA = 6.02214076e+23 # Avogadro's number
h = 6.62607015e-34 # Planck constant
Qe = F / NA # Elementary charge
alpha = 7.2973525693e-3 # Fine-structure constant
c = 299792458 # Speed of light in a vacuum
c2 = c ** 2 # c^2
G = 6.6743e-11 # Gravitational constant
mu0 = (2 * alpha * h) / ((Qe ** 2) * c) # Vacuum permeability
epsilon0 = 1 / (mu0 * c2) # Vacuum permittivity
